/**
  * @brief  中断回调函数
  * @param  *htim
  * @retval 
	* @attention 预分频忼Prescaler＿72-1
  *            计数器溢出值Period＿1000
  *            计算公式 溢出中断频率 = 72M Hz /（Prescaler+1＿/ (Period + 1)
  *                                = 72 000 000 / 72 / 1000 = 1000Hz = 1ms
  */
static uint32_t time = 0;
static float clock_t;
void Clock_Time(void)
{    
		clock_t = 0.05 * Trot.control.speed * time;
		if(clock_t > TIME_S)
			time = 0;
		switch(Trot.control.TimeFlag)
    {
			case TIME_START:  time++;  break;
			case TIME_STOP:		time = time;	break;
			case TIME_CLEAR:	time = 0;	break;
		}
}

